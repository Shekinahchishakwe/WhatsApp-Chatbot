package com.example.demo.model;

public class Message {
	private String from;
    private String body;

    // Getters and Setters
    public void setFrom(String from) {
    	this.from = from;
    }
    public void setBody(String body) {
    	this.body = body;
    }
    public String getFrom() {
    	return this.from;
    }
    public String getBody() {
    	return this.body;
    }
}
